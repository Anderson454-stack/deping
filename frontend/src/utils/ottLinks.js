function normalizeTitle(value) {
  return String(value ?? '').replace(/\s+/g, '').toLowerCase();
}

export function getOttLinks(movieOrTitle) {
  const movie = typeof movieOrTitle === 'object' && movieOrTitle !== null ? movieOrTitle : null;
  const tmdbId = Number(movie?.tmdb_id);
  if (Number.isFinite(tmdbId) && CURATED_OTT_LINKS[tmdbId]) {
    return CURATED_OTT_LINKS[tmdbId];
  }

  const titleKey = normalizeTitle(movie?.title_ko ?? movie?.title ?? movieOrTitle);
  const mappedId = TITLE_TO_ID[titleKey];
  if (mappedId && CURATED_OTT_LINKS[mappedId]) {
    return CURATED_OTT_LINKS[mappedId];
  }

  return [];
}
